

COPY	START	1000

FIRST	STL	RETADR		; SAVE RETURN ADDRESS TO L REGISTER
CLOOP	JSUB	RDREC		; JUMP TO RDREC SUBROUTINE
	LDA	LENGTH
	COMP	ZERO
	JEQ	ENDFIL		; EXIT IF EOF FOUND
	JSUB	WRREC
	J	CLOOP

;	
;	SUBROUTINE INSERT END OF FILE MARKER
;
ENDFIL	LDA	EOF
	STA	BUFFER
	LDA	THREE
	STA	LENGTH
	JSUB	WRREC
	LDL	RETADR
	RSUB
;
;	CONSTANTS
;
EOF	BYTE	C'EOF'
THREE	WORD	3
ZERO	WORD	0
RETADR	RESW	1
LENGTH	RESW	1
BUFFER	RESB	4096

;
;	SUBROUTINE TO READ RECORD INTO BUFFER
;
RDREC	LDX	ZERO		; CLEAR X TO ZERO
	LDA	ZERO		; CLEAR A TO ZERO
RLOOP	TD	INPUT		; TEST INPUT DEVICE
	JEQ	RLOOP		
	RD	INPUT		; READ CHARACTER INTO REGISTER A
	COMP	ZERO		; TEST FOR END OF RECORD (X'00')
	JEQ	EXIT		
	STCH	BUFFER, X	; STROE ONE CHARACTER FROM X TO BUFFER
	TIX	MAXLEN		; LOOP UNLESS MAX LENGTH (X = X + 1, SET EQ IF X == MAXLEN)
	JLT	RLOOP		;     HAS BEEN RECHED
EXIT	STX	LENGTH
	RSUB			; RETURN FROM THIS SUBROUTINE
INPUT	BYTE	X'F1'
MAXLEN	WORD	4096	

;
;	SUBROUTINE TO WRITE RECORD FROM BUFFER
;
WRREC	LDX	ZERO
WLOOP	TD	OUTPUT
	JEQ	WLOOP
	LDCH	BUFFER, X
	WD	OUTPUT
	TIX	LENGTH
	JLT	WLOOP
	RSUB
OUTPUT	BYTE	X'05'
	END	FIRST
